
public class Person {
	
	String lname;
	String fname;
	
	public Person(String lname, String fname) {
		super();
		this.lname = lname;
		this.fname = fname;
	}

	public String getLname() {
		return lname;
	}

	public void setLname(String lname) {
		this.lname = lname;
	}

	public String getFname() {
		return fname;
	}

	public void setFname(String fname) {
		this.fname = fname;
	}
	
	

}
